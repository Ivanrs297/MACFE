def _add(a, b):
    """ Addition """
    return a + b

def _sub(a, b):
    """ Subtract """
    return a - b

def _mul(a, b):
    """ Multiplitcation """
    return a * b

def _div(a, b):
    """ Divison """
    return a / b

def _mod(a, b):
    """ Modulo """
    return a % b

Transformations = [_add, _sub, _mul, _div, _mod]